local str_dis_width = vim.fn.strdisplaywidth

local function longest(dict)
    local length = str_dis_width(dict[1])
    for _, str in ipairs(dict) do
        if str_dis_width(str) > length then
            length = str_dis_width(str)
        end
    end
    return length
end

local function center(dict)
    local new_dict = {}
    local width = longest(dict)
    local space_amount = math.floor(((vim.o.columns - 5) - width) / 2)
    space_amount = space_amount - 2
    for _, v in pairs(dict) do
        local spacing = string.rep(" ", space_amount)
        table.insert(new_dict, spacing .. v)
    end
    return new_dict
end


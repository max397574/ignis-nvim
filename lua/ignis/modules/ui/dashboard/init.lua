local cities = vim.tbl_keys(skylines)
local city = utils.random_element(cities)
local header = skylines[city]

function dashboard.display()
    if
        not (
            (
                vim.api.nvim_buf_get_number(0) > 1
                or vim.api.nvim_buf_get_lines(0, 0, 1, false)[1]:len() == 0
            ) and vim.api.nvim_buf_get_name(0):len() == 0
        )
    then
        return
    end
    vim.defer_fn(function()
        pcall(vim.cmd, "NeorgStart silent=true")
    end, 20)
    -- local buf = vim.api.nvim_create_buf(false, false)
    -- vim.api.nvim_win_set_buf(0, buf)
    vim.api.nvim_buf_set_option(0, "bufhidden", "wipe")
    vim.api.nvim_buf_set_option(0, "buftype", "nofile")
    vim.api.nvim_put(center(header), "l", true, true)
    vim.cmd([[1]])
    for i = 0, #header do
        vim.api.nvim_buf_add_highlight(0, ns, "String", i, 0, -1)
    end
    vim.cmd(
        -- [[silent! setlocal nonu nornu autochdir ft=dashboard nocul laststatus=0 nowrap]]
        [[silent! setlocal nonu nornu ft=dashboard nocul nowrap]]
    )

    vim.keymap.set("n", "?", function()
        local buf = vim.api.nvim_create_buf(false, true)
        vim.api.nvim_buf_set_lines(buf, 0, -1, false, { city })
        vim.api.nvim_buf_add_highlight(buf, ns, "Special", 0, 0, -1)
        vim.api.nvim_open_win(buf, false, {
            relative = "win",
            row = 1,
            col = 5,
            win = 0,
            width = #city + 1,
            height = 1,
            focusable = false,
            style = "minimal",
            border = "rounded",
        })
    end, {
        noremap = true,
        silent = true,
        buffer = true,
    })
    vim.api.nvim_buf_set_keymap(
        0,
        "n",
        "q",
        "<cmd>q!<CR>",
        { noremap = true, silent = true }
    )
end

return dashboard
